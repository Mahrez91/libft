#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	int				i;

	i = 0;
	tmp = s;
	while (n > 0)
	{
		*tmp = (unsigned char)i;
		tmp[i] = '\0';
		tmp++;
		n--;
	}
}
